    public void loadAllGejala() {
        try {
            recGejala = GejalaDAO.getAll();
        } catch (SQLException e) {
            Logger.getLogger(TambahGejalaFrame.class.getName()).log(Level.SEVERE, null, e);
        }
    }

    void tableFill() {
        Object data[][] = new Object[recGejala.size()][3];
        int x = 0, no = 1;
        for (Gejala g : recGejala) {
            data[x][0] = no++;
            data[x][1] = g.getKodeGejala();
            data[x][2] = g.getNamaGejala();
            x++;
        }
        String judul[] = {"No","Kode Gejala", "Nama Gejala"};
        tabelGejala.setModel(new DefaultTableModel(data, judul));
    }

    void initStatus() {
        loadAllGejala();
        tableFill();
    }

